# This code demonstrates the use of different data types in Python.







# Integer
integer_var = 10
print("Integer:", integer_var, type(integer_var))

# Float
float_var = 10.5
print("Float:", float_var, type(float_var))

# String
string_var = "Hello, World!"
print("String:", string_var, type(string_var))

# Boolean
boolean_var = True
print("Boolean:", boolean_var, type(boolean_var))

# List
list_var = [1, 2, 3, 4, 5]
print("List:", list_var, type(list_var))

# Tuple
tuple_var = (1, 2, 3, 4, 5)
print("Tuple:", tuple_var, type(tuple_var))

# Set
set_var = {1, 2, 3, 4, 5}
print("Set:", set_var, type(set_var))

# Dictionary
dict_var = {"key1": "value1", "key2": "value2"}
print("Dictionary:", dict_var, type(dict_var))

# NoneType
none_var = None
print("NoneType:", none_var, type(none_var))


